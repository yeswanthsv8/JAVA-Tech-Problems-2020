//To Find The Length Of The String...

import java.util.*;

class Len
{
   public static void main(String args[])
   {
      String str;
      Scanner sc=new Scanner(System.in);

     System.out.println("Enter The String:");
     str=sc.next();

     int len=str.length();

     System.out.println("The Length Of The String Is:"+len);

  }
}